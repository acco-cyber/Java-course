public class class12 {
    public static void main(String[] args) {
        // Declare multiple variables in one line
        int x = 5, y = 6, z = 50;
        System.out.println(x + y + z);

        // Assign one value to multiple variables
        int a, b, c;
        a = b = c = 50;
        System.out.println(a + b + c);
    }
}